        print("‚¨ÖÔ∏è –õ–µ–≤–µ–µ! –ü—Ä–∏–∑ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –ª–æ–∫–∞—Ü–∏–∏ —Å –º–µ–Ω—å—à–∏–º –Ω–æ–º–µ—Ä–æ–º.")

